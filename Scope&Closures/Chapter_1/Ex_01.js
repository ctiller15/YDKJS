// Left and Right hand lookups.

// Right Hand Side (RHS) reference:

console.log( a );

// Nothing is being assigned to a, and we're just trying to look up and retrieve the value of a.
// That value is then passed to the console.log

// Left hand side (LHS) reference:

a = 2;

// We don't care what a is currently, we just want to assign 2 to it.